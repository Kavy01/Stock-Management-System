    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        String ordid=(String)jcOrdId.getSelectedItem();
        try{
            ArrayList<ProductsPojo> orders = OrderDAO.getOrderDetails(ordid);
            tm = (DefaultTableModel)jtShowStocks.getModel();
            tm.setRowCount(0);
            double sum = 0;
            for (ProductsPojo p : orders)
            {
                Object [] rows = new Object[8];
                rows[0] = p.getProductId();
                rows[1] = p.getProductName();
                rows[2] = p.getProductCompany();
                rows[3] = p.getProductPrice();
                rows[4] = p.getOurPrice();
                rows[5] = p.getQuantity();
                rows[6] = p.getTax()+"%";
                double total1= p.getQuantity()*p.getOurPrice()+(p.getOurPrice()*p.getTax()/100);
                rows[7] = total1;
                tm.addRow(rows);
                sum += total1; 
            }
            lblTotal.setText("Total: "+sum+"");
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error" , "Error!!" , JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnShowActionPerformed

